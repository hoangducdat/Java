public class DieuKienDaDang {
    public static void main(String[] args) {
        double nb1 = 12;
        double nb2 = 10;
        char computer = '/';
        if (computer == '+') {
            System.out.println("nb1 + nb2 =" + (nb1 + nb2));
        } else if (computer == '-') {
            System.out.println("nb1 - nb2 =" + (nb1 - nb2));
        } else if (computer == '*') {
            System.out.println("nb1 * nb2 =" + (nb1 - nb2));
        } else if (computer == '/') {
            System.out.println("nb1 / nb 2 =" + (nb1 / nb2));

        }
    }
}